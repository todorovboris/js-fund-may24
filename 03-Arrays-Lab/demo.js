const num = []; // -> създаване на празен масив;
//МЕТОДИ
num.push(10); // добавяне на елемент в края на масива;
num.push(50); // добавяне на елемент в края на масива;
console.log(num);
console.log(num.includes(5)); // -> търси дали имаме дадения елемент в масива; връща TRUE / FLASE;
console.log(num.toString()); // -> превръща елементите в стринг
console.log(num.join('==')); // -> конкатениране на елементите на масива чрез определен символ(може и спейс) и ги връща, като стринг;
console.log(num.reverse()); // -> обръща реципрочно подредбата на масива: първият става последен, и последният става първи;
