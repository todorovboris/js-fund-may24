// МАСИВИ:

const chisla = [13, 14, 18, 20, 10];

console.log('first el:', chisla[0]); // -> връща първият елемент от масива
console.log(chisla.length); // -> дължината на масива
console.log('last el:', chisla[chisla.length - 1]); // -> връща последният елемент от масива
console.log('------------------------');

//-------------------------------------------------------------------------------------------------

chisla[0] = 54; // -> презаписваме стойността, която се намира на 0-ев индекс;
chisla[chisla.length - 1] = 100; // -> презаписваме стойността на последния възможен индекс със 100
chisla[10] = 1; // -> можем да записваме стойност на индекс, който не е последователен;
console.log(chisla);
console.log('------------------------');

